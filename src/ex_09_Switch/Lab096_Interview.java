package ex_09_Switch;

public class Lab096_Interview {
    public static void main(String[] args) {
        char ch = 'C';
        switch (ch) {
            default:
                System.out.println("Helooooooo");
                break;
            case 'A':
                System.out.println("Character A matched");

                break;
            case 'B':
                System.out.println("Character B matched");
                break;

        }
    }
}
