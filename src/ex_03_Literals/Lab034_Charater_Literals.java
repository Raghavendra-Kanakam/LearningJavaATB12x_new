package ex_03_Literals;

public class Lab034_Charater_Literals {
    public static void main(String[] args) {
        char c1 = '@';
        char c2 = '#';

        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = 'r';

        System.out.println("Raghu"+new_line+"Kanakam");
        System.out.println("Raghavendra"+tab_line+"Kanakam");
        System.out.println("Raghava"+back_space+"Kanakam");
        System.out.println("Raghava"+carriage_return+"Kanakam");
    }
}
