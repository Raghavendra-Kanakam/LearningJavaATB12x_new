package ex_03_Literals;

public class Lab033_Literals {
    public static void main(String[] args) {
        int age = 10; // base system - 10
        int binary_num = 0b101; //Decimal system -10
        int octal = 0101; //Octal base- 8
        int hex_dec = 0x101; //Hex literals -16
        System.out.println(age);
        System.out.println(binary_num);
        System.out.println(octal);
        System.out.println(hex_dec);
    }
}
