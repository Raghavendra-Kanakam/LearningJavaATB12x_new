package ex_10_For_Loop;

public class Lab103_For_If_Loop {
    public static void main(String[] args) {
        for (int i = 0; i < 18; i++) {
            if (i > 15) {
                System.out.println("Gift from papa,New car");
            } else {
                System.out.println("Value of i is:" + i);
            }
        }
    }
}
