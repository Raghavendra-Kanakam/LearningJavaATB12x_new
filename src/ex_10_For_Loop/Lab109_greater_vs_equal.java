package ex_10_For_Loop;

public class Lab109_greater_vs_equal {
    public static void main(String[] args) {
        for (int i= 0;i<=10;i++){ //0 to 10,11 times
            System.out.println("Value of i printed number of times is:" + i);
        }

        for (int j = 1;j<=10;j++) { //1 to 10,10 times
            System.out.println("Value of J printed number of times is:" + j);

        }

        for (int k =0;k<10;k++){  //o to 9 times,10 times
            System.out.println("Value of k printed number of times is:" + k);
        }
    }
}
