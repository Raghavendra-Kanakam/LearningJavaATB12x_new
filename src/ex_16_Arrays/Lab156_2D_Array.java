package ex_16_Arrays;

public class Lab156_2D_Array {
    public static void main(String[] args) {
        int [][] marks = { {1,2,3},
                {4,5,6},
                {7,8,9}
        };

        System.out.println("marks[0][0]--->" + marks[0][0]);
        System.out.println("marks[0][1]--->" + marks[0][1]);
        System.out.println("marks[0][2]--->" + marks[0][2]);
        System.out.println("Second row");
        System.out.println("marks[1][0]--->" + marks[1][0]);
        System.out.println("marks[1][2]--->" + marks[1][1]);
        System.out.println("marks[1][2]--->" + marks[1][2]);
        System.out.println("Thrird row");
        System.out.println("marks[2][0]--->" + marks[2][0]);
        System.out.println("marks[2][1]--->" + marks[2][1]);
        System.out.println("marks[2][2]--->" + marks[2][2]);
    }
}
